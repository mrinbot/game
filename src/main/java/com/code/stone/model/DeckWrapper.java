package com.code.stone.model;

public class DeckWrapper {

	private String suit;
	private String val;
	private int value;
	private String color;

	public String getSuit() {
		return suit;
	}

	public void setSuit(String suit) {
		this.suit = suit;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
	

	public String getVal() {
		return val;
	}

	public void setVal(String val) {
		this.val = val;
	}

	@Override
	public String toString() {
		return "DeckWrapper [suit=" + suit + ", value=" + value + ", color=" + color + "]";
	}


}
